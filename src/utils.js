const getRandomPatient = () => {
  const person = data.patients[Math.floor(Math.random() * 50)];
  return `${person.id}`;
};

const getRandomDentist = () => {
  const person = data.dentists[Math.floor(Math.random() * data.dentists.length)];
  return `${person.id}`;
};

const getPatientName = (patient_id) => {
  const patient = data.patients.find( ({id}) => id === patient_id)
  return patient.name +" "+ patient.surname
}

const getDentistName = (dentist_id) => {
  const dentist = data.dentists.find( ({id}) => id === dentist_id)
  return dentist.name +" "+ dentist.surname
}

const getRandomAssistant = () => {
  const person = data.assistants[Math.floor(Math.random() * data.assistants.length)];
  return `${person.name} ${person.surname}`;
};

const getRandomTime = () => {
  let hour;
  while (true) {
    hour = Math.floor(Math.random() * 24);
    if (hour > 7 && hour < 19) {
      return hour;
    }
  }
};

const getRandomTreatment = () => {
  const treatments = ["cavity", "headremoval", "facings", "needlessdrilling"];
  return treatments[Math.floor(Math.random() * 4)]
}

const getRandomDay = () => Math.floor(Math.random() * 28) + 1;

const checkDuplicates = (newAppointment, allAppointments) => {
  const dentistAppointments = allAppointments.filter(appointment => appointment.dentist === newAppointment.dentist)
  if(allAppointments.find( ({ patient }) => patient === newAppointment.patient && 
  dentistAppointments.find(appointment => appointment.day === newAppointment.day && appointment.time === newAppointment.time))){
    return true;
  }
  return false
}

const generateAppointments = (num) => {
  const allAppointments = []
  while (allAppointments.length < num) {
    const newAppointment = generateRandomAppointment()
    if (checkDuplicates(newAppointment, allAppointments) === false) {
      newAppointment.patient_name = getPatientName(newAppointment.patient)
      newAppointment.dentist_name = getDentistName(newAppointment.dentist)
      allAppointments.push(newAppointment)
    }
  }
  return {appointments: allAppointments.sort((a, b) => a.time - b.time)};
}

const generateRandomAppointment = () => ({
  day: getRandomDay(),
  time: getRandomTime(),
  patient: getRandomPatient(),
  dentist: getRandomDentist(),
  assistant: getRandomAssistant(),
  treatment: getRandomTreatment()
});

export default generateAppointments;